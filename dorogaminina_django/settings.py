def parse_bool(value, default=False):
    if value in (None, ""):
        return default
    return str(value).lower() in ("1", "true", "yes", "on")


def get_config_int(name, default=0):
    value = get_config(name)
    if value in (None, ""):
        return default
    return int(value)


